package ex_02_Variables;

public class Lab012_Datatypes_Integer_Floating_Variables {
    public static void main(String[] args) {
        byte a=10;
        System.out.println(a);
        short b=20;
        System.out.println(b);
        int c=20;
        c=c+2;
        System.out.println(c);
        long phono_number = 9876543210l;
        long phono_number2 = 9876543210L;
        System.out.println(phono_number);
        System.out.println(phono_number2);
        float d= 3.14F;
        System.out.println(d);
    }
}
